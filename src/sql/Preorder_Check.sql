CREATE TABLE Preorder_Check (
	seq INT(11) UNSIGNED NOT NULL AUTO_INCREMENT,
	user_id VARCHAR(255) NOT NULL,
	diction VARCHAR(255) NOT NULL,
	doctor_id VARCHAR(255) NOT NULL,
	preorder_date DATE NOT NULL,
	isend tinyint(1) NOT NULL,
	del tinyint(1) NOT NULL,
	PRIMARY KEY(seq),
	FOREIGN KEY(user_id) REFERENCES member(id) ON UPDATE CASCADE ON DELETE CASCADE,
	FOREIGN KEY(doctor_id) REFERENCES member(id) ON UPDATE CASCADE ON DELETE CASCADE
) ENGINE=InnoDB DEFAULT CHARSET=utf8 COLLATE=utf8_unicode_ci;